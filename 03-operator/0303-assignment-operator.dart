void main()
{
    
  int a = 52;  
  int b = 45;  
  
  a+=b;  
  print("Add and Assign = ${a}");  
  a-=b;  
  print("Subtract and Assign = ${a}");  
  a*=b;  
  print("Multiply and Assign = ${a}");
  a~/=b;  
  print("Divide and Assign = ${a}");  
  a%=b;  
  print("Mod and Assign = ${a}"); 
}

